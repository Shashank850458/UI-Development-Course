
/*
   ++ it + by 1
   -- it - by 1
   for loop syntax
   for(init; condition; ++/--)
   {
      statement
   }
*/

for(let i = 1;i<=10;i++)
{
    console.log("for say:",i);
}


console.log("------desending order----");
for(let i = 10;i>=1;i--)
{
    console.log("for say:",i);
}

console.log("------ +by 2 ----");
for(let i = 1;i<=20;i=i+2)
{
    console.log("for say:",i);
}

console.log("------ -by 2 ----");
for(let i = 20;i>=1;i=i-2)
{
    console.log("for say:",i);
}